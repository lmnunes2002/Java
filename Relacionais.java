package com.mycompany.relacionais;

/**
 *
 * @author Aluno
 */
public class Relacionais {

    public static void main(String[] args) {
        //Declaração de variáveis.
        int a = 10;
        int b = 2;
        int maior;
        
        //Relacionais.
        if (a > b) {
            maior = a;
         } else {
            maior = b;
         }
        
        //Exibindo resultados.
        System.out.println("O maior número é: " + maior);
    }
}
